DROP TABLE IF EXISTS matmp.${hiveconf:prefix}_sms_mt_dropdup;
CREATE TABLE matmp.${hiveconf:prefix}_sms_mt_dropdup AS
SELECT  distinct user_key
       ,${hiveconf:obs_time} AS obs_time 
FROM ${hiveconf:source_tb};

DROP TABLE IF EXISTS matmp.${hiveconf:prefix}_task_sms_content_t1;
CREATE TABLE matmp.${hiveconf:prefix}_task_sms_content_t1 AS
SELECT  user_key,obs_time,job_id,http_timestamp
FROM
(
	SELECT  a.user_key
		,a.obs_time
	       ,b.job_id
	       ,b.http_timestamp
	       ,row_number() over(partition by a.user_key,a.obs_time ORDER BY b.http_timestamp desc) AS rk
              ,datediff(to_date(a.obs_time),to_date(b.http_timestamp)) as obs_days_gap
	FROM matmp.${hiveconf:prefix}_sms_mt_dropdup a
	INNER JOIN fdm.kafka_babel_sms_report_job_inc b -- 短信jobid mapping表
	ON a.user_key = b.user_key
	WHERE a.obs_time >= b.http_timestamp 
) AS t1
WHERE t1.rk = 1 and obs_days_gap<=90;



